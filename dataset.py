import pandas as pd
import matplotlib.pyplot as plt


df = pd.read_csv('T1.csv')  
print(df.head())
print(df.describe())
print(df.isnull().sum())
print(df.info())



plt.figure(figsize=(8, 4))
plt.plot(df['Wind Speed (m/s)'], df['Theoretical_Power_Curve (KWh)'], label='Theoretical Power', color='red', alpha=0.5)  # Points rouges avec transparence
plt.scatter(df['Wind Speed (m/s)'], df['LV ActivePower (kW)'], label='Physical Power', color='blue', alpha=0.5)  # Points bleus avec transparence
plt.title('Theoretical vs Physical Power Output')
plt.xlabel('Wind Speed (m/s)')
plt.ylabel('Power (kW)')
plt.legend()
plt.grid()
plt.show()


plt.figure(figsize=(8, 4))
plt.scatter(df['Wind Direction (°)'], df['Theoretical_Power_Curve (KWh)'], label='Theoretical Power', color='red', alpha=0.5)  # Points rouges avec transparence
plt.scatter(df['Wind Direction (°)'], df['LV ActivePower (kW)'], label='Physical Power', color='blue', alpha=0.5)  # Points bleus avec transparence
plt.title('Theoretical vs Physical Power Output by Wind Direction')
plt.xlabel('Wind Direction (°)')
plt.ylabel('Power (kW)')
plt.legend()
plt.grid()
plt.show()


### Summary in English:

# The two plots compare theoretical power output with the actual power generated by the wind turbine.

#   First plot (by Wind Speed):
#   Shows how power output increases with wind speed. The theoretical curve is smooth, while actual data shows variation, indicating possible inefficiencies or operational issues.

#   Second plot (by Wind Direction):
#   Displays how power output changes with wind direction. Some directions produce less actual power than expected, suggesting potential misalignment or performance issues related to wind direction.

# These visualizations help evaluate the turbine’s efficiency and alignment with wind conditions.
